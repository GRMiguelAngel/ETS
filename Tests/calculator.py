def sum_numbers(num1: int | float, num2: int | float) -> int | float:
    return num1 + num2

def substract(num1: int | float, num2: int | float) -> int | float:
    return num1 - num2

def divide(num1: float, num2:float) -> float:
    return num1 / num2

def multiply(num1: float, num2: float) -> float:
    return num1 * num2